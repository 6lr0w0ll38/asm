.DATA
  A DB 10H, 20H, 30H, 40H
  B DB 50H, 60H, 70H, 80H
  C DB 30H, 40H, 50H, 60H
  D DB "CON GA"
  
.CODE
  START:
  
  MOV AX, @DATA
  MOV DS, AX
  
  MOV CX,4
  LEA SI, B
  LEA DI, D
  
  DOC_MANG:
    MOV AL, [SI]
    MOV [DI], AL
    INC SI
    INC DI
 LOOP DOC_MANG

